#include <libft.h>

void	ft_putlong(long e)
{
	if (e >= 16)
	{
		ft_putlong(ABS(e / 16));
		ft_putlong(ABS(e % 16));
	}
	else
		ft_putchar_fd(ITOA_CHARS[e], 1);
}